lines.push('create table if not exists public.why (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push("  features jsonb default '[]'::jsonb,");
lines.push("  stats jsonb default '[]'::jsonb");
lines.push(');');
lines.push('');
lines.push('-- 12) areas.json -> areas');
lines.push('create table if not exists public.areas (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  city text not null,');
lines.push('  page_index integer not null default 0,');
lines.push('  area_name text not null,');
lines.push('  created_at timestamptz not null default now()');
lines.push(');');
lines.push('create unique index if not exists areas_city_page_name_uidx on public.areas (city, page_index, area_name);');
lines.push('');

lines.push('-- Seed all values from JSON files');
lines.push('truncate table public.blog, public.locations, public.brands, public.contact, public.faq, public.metadata, public.service_details, public.services_commercial, public.services_residential, public.testimonials, public.why, public.areas restart identity;');
lines.push('');

if (blog.length) {
  lines.push('insert into public.blog (id, slug, title, excerpt, category, "readTime", "publishedAt", image, content) values');
  lines.push(blog.map(item => `  (${Number(item.id)}, ${sqlString(item.slug)}, ${sqlString(item.title)}, ${sqlString(item.excerpt)}, ${sqlString(item.category)}, ${sqlString(item.readTime)}, ${sqlString(item.publishedAt)}, ${sqlString(item.image)}, ${sqlString(item.content)})`).join(',\n') + ';');
  lines.push('');
}

const locationRows = Object.entries(locations);
if (locationRows.length) {
  lines.push('insert into public.locations (slug, name, region, description, "mapEmbedUrl", "subAreas") values');
  lines.push(locationRows.map(([slug, item]) => `  (${sqlString(slug)}, ${sqlString(item.name)}, ${sqlString(item.region)}, ${sqlString(item.description)}, ${sqlString(item.mapEmbedUrl)}, ${sqlJson(item.subAreas || [])})`).join(',\n') + ';');
  lines.push('');
}

const brandRows = Object.entries(brands);
if (brandRows.length) {
  lines.push('insert into public.brands (slug, title, description, image, premium) values');
  lines.push(brandRows.map(([slug, item]) => `  (${sqlString(slug)}, ${sqlString(item.title)}, ${sqlString(item.description)}, ${sqlString(item.image)}, ${item.premium ? 'true' : 'false'})`).join(',\n') + ';');
  lines.push('');
}

lines.push(`insert into public.contact (phone, "phoneHref", email, location, "socialMedia", reviews) values (${sqlString(contact.phone)}, ${sqlString(contact.phoneHref)}, ${sqlString(contact.email)}, ${sqlString(contact.location)}, ${sqlJson(contact.socialMedia || [])}, ${sqlJson(contact.reviews || {})});`);
lines.push('');

if (faq.length) {
  lines.push('insert into public.faq (question, answer) values');
  lines.push(faq.map(item => `  (${sqlString(item.question)}, ${sqlString(item.answer)})`).join(',\n') + ';');
  lines.push('');
}

lines.push(`insert into public.metadata (title, description, keywords) values (${sqlString(metadata.title)}, ${sqlString(metadata.description)}, ${sqlString(metadata.keywords)});`);
lines.push('');

const serviceDetailRows = Object.entries(serviceDetails);
if (serviceDetailRows.length) {
  lines.push('insert into public.service_details (slug, title, description, image) values');
  lines.push(serviceDetailRows.map(([slug, item]) => `  (${sqlString(slug)}, ${sqlString(item.title)}, ${sqlString(item.description)}, ${sqlString(item.image || null)})`).join(',\n') + ';');
  lines.push('');
}

if (servicesCommercial.length) {
  lines.push('insert into public.services_commercial (title, description, href, icon) values');
  lines.push(servicesCommercial.map(item => `  (${sqlString(item.title)}, ${sqlString(item.description)}, ${sqlString(item.href)}, ${sqlString(item.icon)})`).join(',\n') + ';');
  lines.push('');
}

if (servicesResidential.length) {
  lines.push('insert into public.services_residential (title, description, href, icon) values');
  lines.push(servicesResidential.map(item => `  (${sqlString(item.title)}, ${sqlString(item.description)}, ${sqlString(item.href)}, ${sqlString(item.icon)})`).join(',\n') + ';');
  lines.push('');
}

if (testimonials.length) {
  lines.push('insert into public.testimonials (initials, name, location, text, avatar) values');
  lines.push(testimonials.map(item => `  (${sqlString(item.initials || null)}, ${sqlString(item.name)}, ${sqlString(item.location || null)}, ${sqlString(item.text)}, ${sqlString(item.avatar || null)})`).join(',\n') + ';');
  lines.push('');
}

lines.push(`insert into public.why (features, stats) values (${sqlJson(why.features || [])}, ${sqlJson(why.stats || [])});`);
lines.push('');

const areaRows = [];
for (const [city, pages] of Object.entries(areas)) {
  if (!Array.isArray(pages)) continue;
  pages.forEach((page, pageIndex) => {
    if (!Array.isArray(page)) return;
    page.forEach((name) => {
      areaRows.push({ city, pageIndex, name });
    });
  });
}
if (areaRows.length) {
  lines.push('insert into public.areas (city, page_index, area_name) values');
  lines.push(areaRows.map(r => `  (${sqlString(r.city)}, ${r.pageIndex}, ${sqlString(r.name)})`).join(',\n') + ';');
  lines.push('');
}

lines.push('-- Default RLS (public read)');
lines.push('do $$');
lines.push('declare');
lines.push('  table_name text;');
lines.push('  table_names text[] := array[');
lines.push("    'contact',");
lines.push("    'blog',");
lines.push("    'services_residential',");
lines.push("    'services_commercial',");
lines.push("    'testimonials',");
