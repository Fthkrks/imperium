const fs = require('fs');
const path = require('path');

const root = process.cwd();
const dataDir = path.join(root, 'data');
const outFile = path.join(root, 'supabase', 'schema.sql');

function readJson(name) {
  return JSON.parse(fs.readFileSync(path.join(dataDir, name), 'utf8'));
}

function sqlString(value) {
  if (value === null || value === undefined) return 'null';
  return `'${String(value).replace(/'/g, "''")}'`;
}

function sqlJson(value) {
  const raw = JSON.stringify(value).replace(/'/g, "''");
  return `'${raw}'::jsonb`;
}

const blog = readJson('blog.json');
const locations = readJson('locations.json');
const brands = readJson('brands.json');
const contact = readJson('contact.json');
const faq = readJson('faq.json');
const metadata = readJson('metadata.json');
const serviceDetails = readJson('service-details.json');
const servicesCommercial = readJson('services-commercial.json');
const servicesResidential = readJson('services-residential.json');
const testimonials = readJson('testimonials.json');
const why = readJson('why.json');
const areas = readJson('areas.json');

