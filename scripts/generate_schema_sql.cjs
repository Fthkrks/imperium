lines.push("    'brands',");
lines.push("    'metadata',");
lines.push("    'service_details'");
lines.push('  ];');
lines.push('begin');
lines.push('  foreach table_name in array table_names');
lines.push('  loop');
lines.push("    execute format('alter table public.%I enable row level security', table_name);");
lines.push('');
lines.push('    if not exists (');
lines.push('      select 1');
lines.push('      from pg_policies');
lines.push("      where schemaname = 'public'");
lines.push('        and tablename = table_name');
lines.push("        and policyname = 'public_read'");
lines.push('    ) then');
lines.push('      execute format(');
lines.push("        'create policy public_read on public.%I for select using (true)',");
lines.push('        table_name');
lines.push('      );');
lines.push('    end if;');
lines.push('  end loop;');
lines.push('end');
lines.push('$$;');

fs.writeFileSync(outFile, lines.join('\n') + '\n', 'utf8');
