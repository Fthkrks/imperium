lines.push('');
lines.push('-- 3) brands.json -> brands');
lines.push('create table if not exists public.brands (');
lines.push('  slug text primary key,');
lines.push('  title text not null,');
lines.push('  description text,');
lines.push('  image text,');
lines.push('  premium boolean default false');
lines.push(');');
lines.push('');
lines.push('-- 4) contact.json -> contact');
lines.push('create table if not exists public.contact (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  phone text,');
lines.push('  "phoneHref" text,');
lines.push('  email text,');
lines.push('  location text,');
lines.push("  \"socialMedia\" jsonb default '[]'::jsonb,");
lines.push("  reviews jsonb default '{}'::jsonb");
lines.push(');');
lines.push('');
lines.push('-- 5) faq.json -> faq');
lines.push('create table if not exists public.faq (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  question text not null,');
lines.push('  answer text not null');
lines.push(');');
lines.push('create unique index if not exists faq_question_uidx on public.faq (question);');
lines.push('');
lines.push('-- 6) metadata.json -> metadata');
lines.push('create table if not exists public.metadata (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  title text not null,');
lines.push('  description text,');
lines.push('  keywords text');
lines.push(');');
lines.push('');
lines.push('-- 7) service-details.json -> service_details');
lines.push('create table if not exists public.service_details (');
lines.push('  slug text primary key,');
lines.push('  title text not null,');
lines.push('  description text,');
lines.push('  image text');
lines.push(');');
lines.push('');
lines.push('-- 8) services-commercial.json -> services_commercial');
lines.push('create table if not exists public.services_commercial (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  title text not null,');
lines.push('  description text,');
lines.push('  href text,');
lines.push('  icon text');
lines.push(');');
lines.push('');
lines.push('-- 9) services-residential.json -> services_residential');
lines.push('create table if not exists public.services_residential (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  title text not null,');
lines.push('  description text,');
lines.push('  href text,');
lines.push('  icon text');
lines.push(');');
lines.push('');
lines.push('-- 10) testimonials.json -> testimonials');
lines.push('create table if not exists public.testimonials (');
lines.push('  id bigint generated by default as identity primary key,');
lines.push('  initials text,');
